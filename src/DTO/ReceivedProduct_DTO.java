package DTO;

/**
 *
 * @author DLCH
 */
public class ReceivedProduct_DTO {
    String productID, productName, size;
    int price;

    public ReceivedProduct_DTO(String productID, String productName, String size, int price) {
        this.productID = productID;
        this.productName = productName;
        this.size = size;
        this.price = price;
    }

    public String getProductID() {
        return productID;
    }

    public void setProductID(String productID) {
        this.productID = productID;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
    
    
}
